type SourceTableType string

const (
	SourceTableTypeTable SourceTableType = "table"
	SourceTableTypeView  SourceTableType = "view"
)

type SourceTableCategory string

const (
	SourceTableCategoryDatamodel           SourceTableCategory = "datamodel"
	SourceTableCategoryKinesisstats        SourceTableCategory = "kinesisstats"
	SourceTableCategoryRds                 SourceTableCategory = "rds"
	SourceTableCategoryRdsClouddbSplitting SourceTableCategory = "rds_clouddb_splitting"
	SourceTableCategoryRdsSharded          SourceTableCategory = "rds_sharded"
	SourceTableCategoryDataStreams         SourceTableCategory = "data_streams"
)

// Table represents a table entry in the non-govramp registry
type Table struct {
	// SourceCatalog is the catalog where the source table resides (typically "default")
	SourceCatalog string

	// SourceSchema is the schema where the source table resides
	SourceSchema string

	// SourceTable is the actual source table name
	SourceTable string

	// TableType specifies the type of table
	TableType SourceTableType

	// OrgIdColumnOverride is the column that contains the org_id
	// Defaults to "org_id". Set this if the org_id column is not called "org_id".
	OrgIdColumnOverride string

	// SamNumberColumn is the column that contains the sam_number
	// Do not set if OrgIdColumnOverride is set.
	SamNumberColumn string

	// TableCategory SourceTableCategory
	TableCategory SourceTableCategory

	// JoinClause is an optional join clause to add to the view definition
	JoinClause string

	// CanReadGroups is a list of groups that can read the table in the non_govramp_customer_data catalog
	CanReadGroups []components.TeamInfo

	// CanReadBiztechGroups is a list of biztech groups that can read the table in the non_govramp_customer_data catalog
	// These groups are not managed by dataplatform like R&D teams in CanReadGroups, but are managed by the biztech team
	// and represent Non-R&D teams that use the biztech workspace.
	CanReadBiztechGroups []string

	// NoOrgSpecificColumn is true if the table does not have a column that can be tied to an org_id or sam_number
	// In this case, we simply create an unfiltered view for the table.
	NoOrgSpecificColumn bool

	// CustomSelectQuery is a custom select query to add to the view definition
	// Columns in the custom select query must be in the same order as the columns in the source table.
	CustomSelectColumns []string

	// TableReferences is a list of tables that are referenced by the view's sql definition.
	// This is used to replace them with its migrated view in non_govramp_customer_data catalog.
	TableReferences []string

	// OrgIdInArray is true if the org_id column is an array
	OrgIdInArray bool

	// SqlFile is the sql file that defines the view
	SqlFile *SqlFile

	// If the GrantAccessToAllGovrampRegistryTeams is set to true, then the table will be granted access
	// for all the teams listed in the CanReadGroups and CanReadBiztechGroups fields in the entire govramp registry.
	// This is used for tables that underly a lot of tables like appconfigs.org_shards_v2, tachographdb_shards.driver_file_downloads, etc.
	GrantAccessToAllGovrampRegistryTeams bool
}

// SqlFile is the sql file that defines the view
type SqlFile struct {
	// SqlFilePath is the path to the sql file that defines the view
	SqlFilePath string

	// CustomViewDefinition is true if the table has a custom view definition
	// This is used to override the default view definition generated by the registry for the table.
	// Note that view definition must also have a LEFT ANTI JOIN to the stateramp_orgs table if filtering is required.
	CustomViewDefinition bool

	// VariableMap is the map of variables to values to replace in the sql file
	VariableMap map[string]string
}

